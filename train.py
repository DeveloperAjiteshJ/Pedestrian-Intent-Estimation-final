def train_epoch(model, train_loader, criterion, optimizer, device, epoch, scaler=None):
    """Train for one epoch with optional mixed precision"""
    model.train()
    
    losses = AverageMeter()
    top1_acc = AverageMeter()
    
    pbar = tqdm(train_loader, desc=f'Epoch {epoch} [TRAIN]')
    
    # Determine device type for autocast
    device_type = 'cuda' if 'cuda' in str(device) else 'cpu'
    
    for frames, labels in pbar:
        frames = frames.to(device)
        labels = labels.to(device)
        
        optimizer.zero_grad()
        
        # Mixed precision training (if scaler provided)
        if scaler is not None:
            with autocast(device_type=device_type):
                logits = model(frames)
                loss = criterion(logits, labels)
            
            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            scaler.step(optimizer)
            scaler.update()
        else:
            # Standard training (without mixed precision)
            logits = model(frames)
            loss = criterion(logits, labels)
            loss.backward()
            optimizer.step()
        
        # Track metrics
        acc = accuracy(logits, labels)[0][0].item()
        losses.update(loss.item(), frames.size(0))
        top1_acc.update(acc, frames.size(0))
        
        pbar.set_postfix({'loss': f'{losses.avg:.4f}', 'acc': f'{top1_acc.avg:.2f}%'})
    
    return losses.avg, top1_acc.avg


def validate(model, val_loader, criterion, device, epoch):
    """Validate model"""
    model.eval()

    losses = AverageMeter()
    top1_acc = AverageMeter()
    all_preds = []
    all_labels = []

    pbar = tqdm(val_loader, desc=f'Epoch {epoch} [VAL]')

    device_type = 'cuda' if 'cuda' in str(device) else 'cpu'

    with torch.no_grad():
        for frames, labels in pbar:
            frames = frames.to(device)
            labels = labels.to(device)

            with autocast(device_type=device_type):
                logits = model(frames)
                loss = criterion(logits, labels)

            preds = torch.argmax(logits, dim=1)
            acc = accuracy(logits, labels)[0][0].item()
            losses.update(loss.item(), frames.size(0))
            top1_acc.update(acc, frames.size(0))
            all_preds.extend(preds.cpu().numpy().tolist())
            all_labels.extend(labels.cpu().numpy().tolist())

            pbar.set_postfix({'loss': f'{losses.avg:.4f}', 'acc': f'{top1_acc.avg:.2f}%'})

    val_macro_f1 = macro_f1_binary(all_labels, all_preds)
    return losses.avg, top1_acc.avg, val_macro_f1


def stratified_split_indices(labels, val_ratio=0.2, seed=42):
    labels = np.array(labels)
    rng = np.random.default_rng(seed)
    train_idx = []
    val_idx = []
    for cls in np.unique(labels):
        cls_idx = np.where(labels == cls)[0]
        rng.shuffle(cls_idx)
        n_val = max(1, int(round(len(cls_idx) * val_ratio)))
        val_idx.extend(cls_idx[:n_val].tolist())
        train_idx.extend(cls_idx[n_val:].tolist())
    rng.shuffle(train_idx)
    rng.shuffle(val_idx)
    return train_idx, val_idx


def train_model(
    model,
    train_loader,
    val_loader,
    num_epochs=30,
    learning_rate=1e-3,
    weight_decay=1e-4,
    device='cuda',
    checkpoint_dir='./checkpoints',
    use_mixed_precision=True,
    class_counts=None
):
    """Full training pipeline with optional mixed precision"""

    os.makedirs(checkpoint_dir, exist_ok=True)
    checkpoint_path = os.path.join(checkpoint_dir, 'best_model.pth')

    if class_counts is None:
        class_counts = [1, 1]
    total = float(sum(class_counts))
    class_weights = torch.tensor([
        total / max(1.0, 2.0 * float(class_counts[0])),
        total / max(1.0, 2.0 * float(class_counts[1]))
    ], dtype=torch.float32).to(device)
    print(f"\n[CLASS WEIGHTS] Dynamic weights from training labels:")
    print(f"  Class 0 count = {class_counts[0]}, weight = {class_weights[0]:.4f}")
    print(f"  Class 1 count = {class_counts[1]}, weight = {class_weights[1]:.4f}")

    criterion = nn.CrossEntropyLoss(weight=class_weights)
    optimizer = optim.Adam(model.parameters(), lr=learning_rate, weight_decay=weight_decay)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=num_epochs)

    scaler = GradScaler() if use_mixed_precision and device == 'cuda' else None
    if scaler:
        print("Using mixed precision training (FP16) for 1.5-2x speedup!")

    best_acc = 0.0
    best_f1 = -1.0
    best_epoch = 0
    start_epoch = 0

    if os.path.exists(checkpoint_path):
        try:
            ckpt = torch.load(checkpoint_path, map_location=device)
            if 'model_state_dict' in ckpt:
                model.load_state_dict(ckpt['model_state_dict'])
            if 'optimizer_state_dict' in ckpt:
                optimizer.load_state_dict(ckpt['optimizer_state_dict'])
            best_acc = float(ckpt.get('best_acc', 0.0))
            best_f1 = float(ckpt.get('best_f1', -1.0))
            best_epoch = int(ckpt.get('epoch', 0))
            start_epoch = best_epoch + 1
            print(f"[RESUME] Loaded checkpoint from {checkpoint_path}")
            print(f"         Resume from epoch {start_epoch} | best_acc={best_acc:.2f}% best_f1={best_f1:.4f}")
        except Exception as e:
            print(f"[WARN] Could not resume from checkpoint: {e}")
            # If resume fails, just train from scratch
            start_epoch = 0

    print("=" * 80)
    print("Starting training (best checkpoint selected by validation accuracy)...")
    print("=" * 80)

    for epoch in range(start_epoch, num_epochs):
        train_loss, train_acc = train_epoch(model, train_loader, criterion, optimizer, device, epoch, scaler)
        val_loss, val_acc, val_f1 = validate(model, val_loader, criterion, device, epoch)
        scheduler.step()

        print(f"\nEpoch {epoch:3d}: "
              f"train_loss={train_loss:.4f} train_acc={train_acc:.2f}% | "
              f"val_loss={val_loss:.4f} val_acc={val_acc:.2f}% val_f1={val_f1:.4f}")

        if val_acc > best_acc or (val_acc == best_acc and val_f1 > best_f1):
            best_acc = val_acc
            best_f1 = val_f1
            best_epoch = epoch
            checkpoint_path = os.path.join(checkpoint_dir, 'best_model.pth')
            torch.save({
                'epoch': epoch,
                'model_state_dict': model.state_dict(),
                'optimizer_state_dict': optimizer.state_dict(),
                'best_acc': best_acc,
                'best_f1': best_f1
            }, checkpoint_path)
            print(f"✓ Saved best model (acc={best_acc:.2f}%, f1={best_f1:.4f}) to {checkpoint_path}")

    print("\n" + "=" * 80)
    print(f"Training complete! Best accuracy: {best_acc:.2f}% | Best macro-F1: {best_f1:.4f} at epoch {best_epoch}")
    print("=" * 80)

    return model


