def main():
    """Main training entry point"""

    REPO_ROOT = Path(__file__).resolve().parent
    PIE_ROOT = str(REPO_ROOT)
    TRAIN_PKL = str(REPO_ROOT / 'data_cache' / 'sequences' / 'train_sequences.pkl')
    TEST_PKL = str(REPO_ROOT / 'data_cache' / 'sequences' / 'test_sequences.pkl')

    BATCH_SIZE = 16
    NUM_EPOCHS = 50
    LEARNING_RATE = 1e-3
    WEIGHT_DECAY = 5e-4
    NUM_WORKERS = 0
    MAX_FRAMES = 4
    USE_MIXED_PRECISION = False
    DEVICE = 'cuda' if torch.cuda.is_available() else 'cpu'

    print("=" * 80)
    print("TinyMobileNet-XS Training")
    print("=" * 80)
    print(f"Device: {DEVICE}")
    print(f"Batch size: {BATCH_SIZE}")
    print(f"Epochs: {NUM_EPOCHS}")
    print(f"Learning rate: {LEARNING_RATE}")
    print(f"Weight decay: {WEIGHT_DECAY}")
    print(f"Mixed precision: {USE_MIXED_PRECISION}")
    print(f"Max frames per sequence: {MAX_FRAMES}")
    print()
    print("✅ Using internal stratified 80/20 split from train_sequences.pkl")
    print("✅ External set05 kept only for final evaluation / export")
    print("=" * 80)

    print("\nLoading base training dataset...")
    full_dataset = PIEIntentionDataset(
        TRAIN_PKL,
        PIE_ROOT,
        max_frames=MAX_FRAMES,
        split='train_full'
    )
    external_test_dataset = PIEIntentionDataset(
        TEST_PKL,
        PIE_ROOT,
        max_frames=MAX_FRAMES,
        split='test_external'
    )

    full_labels = [int(x[0][0]) for x in full_dataset.labels]
    train_indices, val_indices = stratified_split_indices(full_labels, val_ratio=0.2, seed=42)
    train_dataset = Subset(full_dataset, train_indices)
    val_dataset = Subset(full_dataset, val_indices)

    train_labels = [full_labels[i] for i in train_indices]
    val_labels = [full_labels[i] for i in val_indices]
    class_0_count = sum(lbl == 0 for lbl in train_labels)
    class_1_count = sum(lbl == 1 for lbl in train_labels)

    print(f"\nSplit summary:")
    print(f"  Train samples: {len(train_indices)} | class0={sum(l == 0 for l in train_labels)} class1={sum(l == 1 for l in train_labels)}")
    print(f"  Val samples:   {len(val_indices)} | class0={sum(l == 0 for l in val_labels)} class1={sum(l == 1 for l in val_labels)}")
    print(f"  External test samples: {len(external_test_dataset)}")

    train_loader = DataLoader(
        train_dataset,
        batch_size=BATCH_SIZE,
        shuffle=True,
        num_workers=NUM_WORKERS,
        pin_memory=True
    )
    val_loader = DataLoader(
        val_dataset,
        batch_size=BATCH_SIZE,
        shuffle=False,
        num_workers=NUM_WORKERS,
        pin_memory=True
    )
    test_loader = DataLoader(
        external_test_dataset,
        batch_size=BATCH_SIZE,
        shuffle=False,
        num_workers=NUM_WORKERS,
        pin_memory=True
    )

    print("\nCreating model...")
    model = create_model(num_classes=2, t_frames=MAX_FRAMES)
    model = model.to(DEVICE)

    model = train_model(
        model,
        train_loader,
        val_loader,
        num_epochs=NUM_EPOCHS,
        learning_rate=LEARNING_RATE,
        weight_decay=WEIGHT_DECAY,
        device=DEVICE,
        use_mixed_precision=USE_MIXED_PRECISION,
        class_counts=[class_0_count, class_1_count]
    )

    print("\nQuantizing and exporting for FPGA...")
    quantize_and_export(
        model,
        test_loader,
        device=DEVICE,
        output_dir='./fpga_weights'
    )

    print("\n" + "=" * 80)
    print("✓ Training complete!")
    print(f"✓ Best model: checkpoints/best_model.pth")
    print(f"✓ FPGA weights: fpga_weights/tinymobilenet_xs_weights.h")
    print(f"✓ Use in RTL: copy tinymobilenet_xs_weights.h to Vivado project")
    print("=" * 80)


if __name__ == '__main__':
    main()
