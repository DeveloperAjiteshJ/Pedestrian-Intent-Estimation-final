class PIEIntentionDataset(Dataset):
    """
    PyTorch Dataset for PIE Pedestrian Intention Estimation
    
    Loads from .pkl files generated by generate_pie_database.py
    """
    
    def __init__(self, pkl_file, pie_root_path, max_frames=4, transform=None, split='train'):
        """
        Args:
            pkl_file: Path to train_sequences.pkl or test_sequences.pkl
            pie_root_path: Root path to PIE dataset
            max_frames: Maximum frames per sequence (clips longer sequences)
            transform: torchvision transforms
            split: 'train' or 'test'
        """
        self.pkl_file = pkl_file
        self.pie_root = pie_root_path
        self.max_frames = max_frames
        self.transform = transform
        self.split = split
        
        # Load pickle file
        with open(pkl_file, 'rb') as f:
            data = pickle.load(f)
        
        self.image_sequences = data['image']  # List of frame path sequences
        self.labels = data['intention_binary']  # List of labels
        
        # Statistics tracking
        self.real_images_loaded = 0
        self.black_frames_used = 0
        self.total_frames_processed = 0
        self.failed_paths = []
        
        print(f"Loaded {len(self.image_sequences)} samples from {split} set")
        
        # Print class distribution
        labels_flat = [int(x[0][0]) for x in self.labels]
        crossing = sum(labels_flat)
        not_crossing = len(labels_flat) - crossing
        print(f"  Class distribution: {not_crossing} not-crossing, {crossing} crossing")
        
        print(f"\n⏳ [Dataset] Starting to load images...")
        print(f"   This will take a moment to scan all paths...\n")
    
    def __len__(self):
        return len(self.image_sequences)
    
    def __getitem__(self, idx):
        """
        Returns:
            frames: (T, 3, 64, 64) - temporal sequence of RGB frames
            label: int - binary label (0=not crossing, 1=crossing)
        """
        image_paths = self.image_sequences[idx]
        label = int(self.labels[idx][0][0])  # Extract label from nested list
        
        # Limit to max_frames
        image_paths = image_paths[:self.max_frames]
        num_frames = len(image_paths)
        
        # Load frames
        frames = []
        
        for path in image_paths:
            img_loaded = False
            try:
                # First try original path
                if os.path.exists(path):
                    img_path = path
                    img_loaded = True
                else:
                    # Path doesn't exist, try to reconstruct it correctly
                    path_obj = Path(path)
                    parts = path_obj.parts
                    
                    # Extract filename and folder names
                    filename = path_obj.name  # e.g., "04511.png"
                    
                    # Find set (set01, set02, etc) and video folder
                    set_name = None
                    video_name = None
                    
                    for part in parts:
                        if part.startswith('set') and len(part) == 5 and part[3:].isdigit():
                            set_name = part
                        if part.startswith('video') and '_' in part:
                            video_name = part
                    
                    # Reconstruct correct path
                    if set_name and video_name:
                        img_path = os.path.join(self.pie_root, 'images', set_name, video_name, filename)
                        
                        if not os.path.exists(img_path):
                            # Try without underscore in video name
                            video_name_alt = video_name.replace('_', '')
                            img_path = os.path.join(self.pie_root, 'images', set_name, video_name_alt, filename)
                            
                            if not os.path.exists(img_path):
                                # Last resort: search in all subdirectories
                                found = False
                                for root, dirs, files in os.walk(os.path.join(self.pie_root, 'images')):
                                    if filename in files:
                                        img_path = os.path.join(root, filename)
                                        found = True
                                        img_loaded = True
                                        break
                                
                                if not found:
                                    # ❌ IMAGE NOT FOUND
                                    self.total_frames_processed += 1
                                    self.black_frames_used += 1
                                    if path not in self.failed_paths:
                                        self.failed_paths.append(path)
                                        print(f"❌ [FAILED] Image not found: {filename}")
                                    frames.append(np.zeros((64, 64, 3), dtype=np.float32))
                                    continue
                            else:
                                img_loaded = True
                        else:
                            img_loaded = True
                    else:
                        # ❌ CANNOT RECONSTRUCT PATH
                        self.total_frames_processed += 1
                        self.black_frames_used += 1
                        if path not in self.failed_paths:
                            self.failed_paths.append(path)
                            print(f"❌ [FAILED] Cannot parse path: {path}")
                        frames.append(np.zeros((64, 64, 3), dtype=np.float32))
                        continue
                
                # Load image from reconstructed/original path
                if img_loaded:
                    img = Image.open(img_path).convert('RGB')
                    
                    # Resize to 64×64 if needed
                    if img.size != (64, 64):
                        img = img.resize((64, 64), Image.BILINEAR)
                    
                    # Convert to numpy array
                    img_array = np.array(img, dtype=np.float32) / 255.0  # Normalize to [0, 1]
                    
                    # Check if image is mostly black (loading issue)
                    mean_val = np.mean(img_array)
                    if mean_val < 0.05:
                        print(f"⚠️  [BLACK IMAGE] {Path(img_path).name} (mean={mean_val:.3f}) - Might be empty!")
                        self.black_frames_used += 1
                    else:
                        self.real_images_loaded += 1
                    
                    # Apply transform if any
                    if self.transform:
                        img_array = self.transform(img_array)
                    
                    frames.append(img_array)
                    self.total_frames_processed += 1
            
            except Exception as e:
                # ❌ ERROR LOADING IMAGE
                self.total_frames_processed += 1
                self.black_frames_used += 1
                if path not in self.failed_paths:
                    self.failed_paths.append(path)
                    print(f"❌ [ERROR] Could not load {Path(path).name}: {str(e)[:50]}")
                frames.append(np.zeros((64, 64, 3), dtype=np.float32))
        
        # Convert to torch tensor
        frames = np.stack(frames, axis=0)  # (T, 64, 64, 3)
        frames = torch.from_numpy(frames).permute(0, 3, 1, 2)  # (T, 3, 64, 64)
        
        # Pad to max_frames if needed
        if num_frames < self.max_frames:
            padding = torch.zeros(self.max_frames - num_frames, 3, 64, 64)
            frames = torch.cat([frames, padding], dim=0)
        
        # Print statistics every 100 samples
        if (idx + 1) % 100 == 0:
            real_pct = 100 * self.real_images_loaded / max(1, self.total_frames_processed)
            black_pct = 100 * self.black_frames_used / max(1, self.total_frames_processed)
            print(f"📊 [Progress] Processed {idx+1}/{len(self)} samples")
            print(f"   ✅ Real images: {self.real_images_loaded} ({real_pct:.1f}%)")
            print(f"   ⚫ Black frames: {self.black_frames_used} ({black_pct:.1f}%)")
            print(f"   ❌ Failed paths: {len(self.failed_paths)}")
        
        return frames, torch.tensor(label, dtype=torch.long)


